/*                 Q.8: Write a program to find the circumference of a circle with radius 10 in C++         */

#include <iostream>
using namespace std;

main() {
    float pi = 3.14; // Value of Pi
    float radius = 10; // Radius of the circle

    // Calculate circumference
    float circumference = 2 * pi * radius;

    // Output the result
    cout << "Circumference of the circle with radius 10: " << circumference << endl;

}
/*
    output:-
        Circumference of the circle with radius 10: 62.8
*/