/*                     Q.6: How can you output “Alpha” and “Intern” in two different lines in C++?          */
#include <iostream>
using namespace std;

int main() {
    cout << "Alpha" << endl;  // Output "Alpha" and move to the next line
    cout << "Intern" << endl; // Output "Intern" and move to the next line

    return 0;
}
/*
    output:-
        Alpha
        Intern
*/
